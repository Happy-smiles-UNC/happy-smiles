How To Use a Manual Toothbrush?
      
Proper Angle: Aim the bristles at a 45-degree angle towards the gumline. This angle helps effectively remove plaque without causing irritation to your gums.

Brushing Technique: 
1. Place the brush at 45 degree angle, brush against the gumline to get rid of any trapped plaque or food debris
2. Move the brush in small circular motion
3. Clean the inside surfaces of back of the teeth moving the brush back and front
4. Clean the inside surfaces of the front upper teeth by tilting the brush vertically up and down
5. Clean the inside surface of the front lower teeth by tilting the brush vertically up and down
6. Move the brush in a back and forth motion to clean biting surfaces
    
Time Allocation: Divide your mouth into four quadrants: upper left, upper right, lower left, and lower right. Spend at least 30 seconds brushing each quadrant, ensuring thorough cleaning for a total of 2 minutes.
Frequency of Brushing: Brush at least twice a day, ideally in the morning and before bedtime, to maintain optimal oral hygiene.
Toothbrush Maintenance: Replace your toothbrush or toothbrush head every 3 months, or sooner if the bristles become frayed. Regular replacement ensures your brush is effective in cleaning your teeth and maintaining oral health.

How To Use An Electric Toothbrush?

Proper Positioning: Position the bristles at a 45-degree angle to the gums to enhance cleaning along the gumline. 
Brushing Technique: Hold the brush head against each tooth individually and allow the toothbrush to rotate and vibrate; avoid scrubbing. 
Time Allocation: Divide the mouth into four quadrants: upper left, upper right, lower left, and lower right. Spend 30 seconds on each quadrant for a total brushing time of 2 minutes. 
Frequency of Brushing: Brush twice daily, preferably in the morning and before bedtime, to maintain optimal oral hygiene. 
Toothbrush Maintenance: Replace the electric toothbrush head every 3 months, or sooner if the bristles show signs of wear.

      
